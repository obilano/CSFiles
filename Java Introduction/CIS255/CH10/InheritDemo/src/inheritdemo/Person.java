package inheritdemo;

public class Person 
{
    private String fName;
    private String lName;
    
    public void setFName(String newName)
    {
        fName = newName;
    }
    public String getFName()
    {
        return fName;
    }
    public void setLName(String newName)
    {
        lName = newName;
    }
    public String getLName()
    {
        return lName;
    }
    

}
